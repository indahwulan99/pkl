		<div class="col">
			<p> Menurut Euramet Metrologi atau ilmu pengukuran merupakan disiplin ilmu pengetahuan
			yang mendalami bagaimana metode pengukuran, kalibrasi dan akurasi baik di industri, 
			ilmu pengetahuan dan teknologi.</p>
			<p>UPTD Metrologi Legal mempunyai tugas melaksanakan kebijakan teknis operasional dan/atau 
			kegiatan teknis penunjang dalam hal pelayanan tera dan tera ulang alat-alat UTTP,
			pengawasan UTTP dan barang dalam keadaan terbungkus, ketatausahaan serta pelayanan 
			kemetrologian legal lainnya pada Dinas Perdagangan dan mengelola UPTD Metrologi Legal.</p>
			<p>Untuk melaksanakan tugas sebagaimana dimaksud, UPTD Metrologi Legal menyelenggarakan fungsi:</p> 
			<p>a. penyusunan rencana/program pembinaan, pelaksanaan, pengelolaan, dan pemeliharaan standar kerja;</p>
			<p>b. penyusunan rencana/program tera/tera ulang alat-alat UTTP;</p>
			<p>c. pemeliharaan ketertelusuran standar kerja dan perlengkapannya;</p>
			<p>d. pelaksanaan pengelolaan, pemeliharaan, dan bertanggung jawab terhadap penggunaan cap tanda tera (CTT);</p>
			<p>e. pelaksanaan tera/tera ulang alat-alat UTTP;</p>
			<P>f. pelaksanaan pengawasan UTTP dan barang dalam keadaan terbungkus (BDKT), dan satuan ukuran,
			penyuluhan, pengamatan serta penyidikan tindak pidana dibidang metrologi legal;</P>
			<P>g. pelaksanaan monitoring dan evaluasi serta laporan;dan</P>
			<p>h. pelaksanaan tugas lain yang diberikan oleh Kepala Dinas sesuai dengan peraturan perundang-undangan.</p>

		</div>
		<div class="col">
		<p>Kepala UPTD Metrologi Legal mempunyai tugas:</p>
		<p>a. memimpin, mengkoordinasikan, mengawasi dan mengendalikan seluruh kegiatan tera/tera ulang alat-alat UTTP;</p>
		<P>b. mengawasi UTTP dan barang dalam keadaan terbungkus (BDKD serta sistem satuan ukuran;</P>
		<p>c. melaporkan pelaksanaan tugas-tugas lain yang diberikan oleh Kepala Dinas</p>
		<p>Kelompok Jabatan Fungsional UPTD Metrologi Legal mempunyai tugas:</p>
		<p>a. melaksanakan pengelolaan instalasi uji;</p>
		<p>b. melaksanakan pengelolaan peralatan atau perlengkapan standar tera/tera ulang UTTP;</p>
		<p>c. melaksanakan tera/tera ulang alat-alat UTTP;</p>
		<p>d. melaksanakan pengujian alat-alat UTTP;</p>
		<p>e. melaksanakan pengelolaan cap tanda tera (CT.D;</p>
		<p>f. melaksanakan pengawasan alat UTTP;</p>
		<p>g. melaksanakan penyuluhan kemetrologian;dan</p>
