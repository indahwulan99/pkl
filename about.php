<p>“Terwujudnya Ketahanan Pangan bagi Masyarakat Kabupaten Kediri yang Religius, Cerdas, Sehat, Sejahtera, Kreatif, dan Berkeadilan, yang didukung oleh Aparatur Pemerintah yang Profesional”</p>				
