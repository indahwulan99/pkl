    <h3>MISI</h3>
<p>Untuk mewujudkan Visi tersebut, ditetapkan Misi pembangunan Kabupaten Kediri tahun 2016 – 2021 sebagai berikut :</p>

<p>1. Melaksanakan ajaran agama dan/atau kepercayaan dalam kehidupan bermasyarakat yang penuh toleransi, tenggang rasa, dan harmoni.</p>
<p>2. Mempercepat pembangunan di sektor pertanian, peternakan, perikanan, dan perkebunan untuk memperkuat kemandirian masyarakat menuju swasembada pangan</p>
<p>3. Meningkatkan kesejahteraan masyarakat yang ditandai dengan terpenuhinya kebutuhan pangan, sandang, dan papan dalam lingkungan masyarakat yang tertib dan aman.</p>
<p>4. Menumbuhkembangkan layanan pendidikan murah (terjangkau) dan berkualitas pada semua jenis, jenjang dan jalur pendidikan.</p>
<p>5. Mewujudkan Masyarakat yang mandiri dan berkeadilan di bidang kesehatan</p>
<p>6. Menumbuhkembangkan kreatifitas, produktifitas dan pendapatan masyarakat melalui kebijakan ekonomi kerakyatan dengan memajukan industri menengah, kecil dan mikro.</p>
<p>7. Melanjutkan pembangunan kepariwisataan dan kebudayaan sebagai upaya meningkatkan ekonomi masyarakat dan melestarikan budaya daerah.</p>
<p>8. Mengembangkan koperasi sebagai salah satu soko guru pembangunan ekonomi kerakyatan.</p>
<p>9. Mengoptimalkan pelayanan perizinan bagi kepentingan kehidupan masyarakat, terutama dalam menggiatkan investasi dan dunia usaha.</p>
<p>10. Memantapkan pembangunan kependudukan, yang meliputi ketertiban sistem pendataan dan pemberdayaan warga masyarakat terutama di wilayah pedesaan, khususnya kaum perempuan</p>
<p>11. Mewujudkan aparatur pemerintah yang profesional dan melanjutkan reformasi birokrasi</p>
<p>12. Membangun infrastruktur penunjang pembangunan di berbagai bidang.</p>
<p>13. Membangun dan mengembangkan jaringan sistem informasi dan komunikasi</p>
<p>14. Meningkatkan pembangunan lingkungan hidup yang sehat, serasi dan seimbang</p>
<p>15. Pembangunan sektor ketenagakerjaan untuk kesejahteraan masyarakat</p>
